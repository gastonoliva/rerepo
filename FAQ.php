<details>
   <summary>¿Como funciona el Trade by pay?</summary>
    <p> Trade by pay es un nuevo metodo de pago bastante simple que consiste en el intercambio de productos que se estan vendiendo como forma de pago, como una especie de canje. Los usuarios pueden optar por intercambiar los productos entre si e incluso combinarlo con otras formas de pago (siempre y cuando ambas partes esten de acuerdo con el intercambio).  </p>
</details>
<details>
    <summary>¿Como protegemos a los vendedores?</summary>
      <p>Si surge algún problema con una compra, es posible que alguien te haga un reclamo.<br><br>No te preocupes, la mayoría de las veces se trata de simples malentendidos que se resuelven rapidísimo.<br><br>Te avisamos apenas recibís el reclamo y te decimos cómo seguir. Si es necesario, podemos mediar para ayudarte a llegar a una solución.</p>
</details>
<details>
    <summary>¿Que sucede si el comprador desconoce el pago?</summary>
      <p>Si bien no es común, es posible que algún comprador desconozca el pago que hizo con su tarjeta de crédito.<br><br> A ese desconocimiento lo llamamos “contracargo” y puede pasar porque alguien utilizó la tarjeta de crédito del comprador sin autorización o el comprador considera que no cumpliste con lo acordado.<br><br>No te preocupes. Para que puedas conservar el dinero de tu venta, creamos el Programa de Protección al Vendedor.<br><br>Si recibís un contracargo, te contactamos, te pedimos que compruebes la entrega del producto y luego veremos cada caso individualmente.</p>
</details>
<details>
    <summary>¿Como protegemos a los compradores?</summary>
      <p>Cuando pagás, el vendedor puede ver el dinero en su cuenta, pero tiene que esperar para poder usarlo. Lo hicimos así para que tengas tiempo de recibir el producto y evaluarlo.<br><br>Además, podés elegir que te entreguen el producto a través de Sending by trade y tu dinero va a estar protegido hasta 10 días después de que el correo nos avise que te llegó el producto. </p>
</details>
<details>
      <summary>¿Que hacer si ocurre un inconveniente con la compra?</summary>
    <p>Aunque no es común, si tuvieras algún problema con la compra, estaremos ahí para ayudarte.<br><br> Lo primero que tenés que hacer es iniciar un reclamo a tu vendedor. Es importante que lo hagas lo antes posible y nos cuentes en detalle qué pasó, para que podamos seguir tu problema y ayudarte a resolverlo.<br><br> Si pagás por cualquier metodo electronico y llegaras a tener un problema, te devolvemos el 100% del dinero porque tu compra está siempre protegida.</p>
